<?php 
function perimetroQ($b){
	$perimetroQ = 4 * $b;
	return $perimetroQ;
}
function perimetroR($b,$a){
	$perimetroR = ($b * 2) + ($a * 2);
	return $perimetroR;
}
function perimetroT($a,$b,$c,$B){
	$perimetroT = $a + $b + $c + $B;
	return $perimetroT;
}
function perimetroP($a,$b){
	$perimetroP = 2*($a+$b);
	return $perimetroP;
}

?>